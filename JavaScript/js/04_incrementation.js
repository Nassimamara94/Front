/* ---------------------------------
        INCREMENTATION
------------------------------------- */
//  C'est l'opération qui consiste à ajouter 1 (une valeure entière), à son opérande et renvoie une valeur. L'inverse est apppelé décrementation. 
var nb1 = 1; 
nb1 = nb1 + 1;
console.log(nb1); // nb1 = 2 maintenant

//ECRITURES SIMPLIFIEES


nb1++
console.log(nb1); // nb1 = 3 maintenant

++nb1;
console.log(nb1); // nb1 = 4 maintenant

// DECREMENTATION


nb1 = nb1 -1;
console.log(nb1); // nb1 = 3 maintenant

nb1--
console.log(nb1); // nb1 = 2 maintenant




